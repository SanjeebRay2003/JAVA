package Advance_Java.Collection_Framework.Sets.Hash_Set;

import java.util.HashSet;

public class Hash_set {
    public static void main(String[] args) {

        HashSet<Integer> hash = new HashSet<>();

        hash.add(10);
        hash.add(20);
        hash.add(30);
        hash.add(40);
        hash.add(50);
        hash.add(60);

        System.out.println(hash);

    }
}
